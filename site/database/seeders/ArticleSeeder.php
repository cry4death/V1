    private function contentHealthyHeartSigns(): string
    {
        return <<<'HTML'
<p>Сердечно-сосудистые заболевания остаются ведущей причиной смертности в популяции; при этом значительная часть случаев связана с модифицируемыми факторами риска и может быть отсрочена при раннем выявлении нарушений. Ниже приведены <strong>десять клинико-бытовых критериев</strong>, которые у лиц без установленного кардиологического диагноза чаще сочетаются с удовлетворительной компенсацией гемодинамики и переносимостью нагрузки. Перечень носит ориентировочный характер: интерпретация возможна только с учётом анамнеза, сопутствующей патологии и назначенной терапии.</p>

<h2>1. Частота сердечных сокращений (ЧСС) в покое в пределах возрастной нормы</h2>
<p>У взрослых при отсутствии аритмий и без приёма препаратов, влияющих на ритм, в покое обычно ожидается ЧСС приблизительно <strong>60–90 уд./мин</strong>; у тренированных лиц допустимы более низкие значения. Устойчивая тахикардия в покое, нерегулярный ритм, выраженная экстрасистолия, субъективно переживаемая как «перебои», требуют записи ЭКГ и очной оценки.</p>

<h2>2. Артериальное давление (АД) без признаков некомпенсированной гипертензии</h2>
<p>Ориентиром для офисного измерения у многих пациентов остаются значения <strong>&lt;140/90 мм рт. ст.</strong> (индивидуальные целевые уровни задаёт лечащий врач). Стабильно повышенные цифры, утренние «скачки», головная боль или головокружение на фоне измерений — показание к обследованию, а не к самонаблюдению без консультации.</p>

<h2>3. Сохранённая переносимость привычной физической нагрузки</h2>
<p>Способность без симптомов выполнять обычный объём ходьбы, подъёма по лестнице (1–2 пролёта) и бытовой активности соответствует, как правило, <strong>I функциональному классу</strong> по переносимости нагрузки в бытовой оценке. Снижение толерантности к нагрузке по сравнению с предыдущим уровнем (клиника нарастающей сердечной недостаточности или ишемии) всегда должно быть разобрано врачом.</p>

<h2>4. Отсутствие типичной стенокардии напряжения</h2>
<p>Кардиальный дискомфорт нередко описывают как давление, сжигание или тяжесть за грудиной с иррадиацией в левую руку, нижнюю челюсть, межлопаточную область, с провокацией физическим или эмоциональным напряжением и купированием покоем. <strong>Повторяющиеся эпизоды</strong> такого характера — повод к немедленной диагностике (ЭКГ, лаборатория, по показаниям — холтер, проба с нагрузкой, визуализация коронарного русла).</p>

<h2>5. Адекватное восстановление ЧСС после нагрузки</h2>
<p>После прекращения умеренной аэробной нагрузки частота пульса снижается в течение нескольких минут без выраженной астении, пресинкопальных состояний и выраженной одышки. Затянутое восстановление или одышка на малой нагрузке могут отражать нарушение автоматики, ишемию или снижение насосной функции миокарда.</p>

<h2>6. Ночной отдых без ортопноэ и приступов пароксизмальной одышки</h2>
<p>Вынужденное положение при лёгочном застое, пробуждения с ощущением нехватки воздуха, нарастающая одышка в горизонтальном положении относятся к «красным флагам» застойной сердечной недостаточности. Сочетание выраженного храпа, апноэ сна и дневной сонливости также повышает кардиоваскулярный риск и требует отдельного обследования.</p>

<h2>7. Отсутствие периферических отёков без установленной причины</h2>
<p>Стойкие симметричные отёки голеней к вечеру, прогрессирующая прибавка массы тела на фоне жажды и полидипсии, асимметричный отёк конечности относят к симптомам, исключающим самостоятельную «оценку по отсутствию жалоб». Локальный отёк после длительного вертикального положения у отдельных лиц может быть функциональным, но дифференциальный диагноз остаётся за врачом.</p>

<h2>8. Отсутствие синкопальных эпизодов и нестабильной гемодинамики</h2>
<p>Кратковременное ортостатическое головокружение при резкой смене положения отличается от <strong>обмороков</strong> и пресинкопе неясного генеза. Рецидивирующий синкопе, палпитации с предобморочным состоянием, внезапная потеря сознания на фоне нагрузки — показания к срочной кардиологической и/или неврологической оценке.</p>

<h2>9. Контроль модифицируемых факторов риска по принципам первичной профилактики</h2>
<p>Согласно общепринятым подходам к первичной профилактике ССЗ (в том числе рекомендациям ВОЗ по снижению потребления соли, отказу от табака, достаточной физической активности и сбалансированному питанию), к благоприятному профилю относят отсутствие курения, умеренное потребление алкоголя, индекс массы тела и обмен глюкозы/липидов в зоне целевых значений, назначенных при диспансерном наблюдении.</p>

<h2>10. Регулярное профилактическое обследование без выявления значимой патологии</h2>
<p>Объём скрининга определяется возрастом и сопутствующими заболеваниями; базово могут рассматриваться измерение АД, липидограмма, глюкоза натощак или HbA1c, ЭКГ, оценка факторов риска. Отсутствие отклонений на момент обследования не исключает развитие заболевания; интервалы контроля задаёт врач.</p>

<h2>Показания к очной консультации без отлагательства</h2>
<ul>
    <li>Боль или дискомфорт в груди, иррадиация, симптомы, напоминающие стенокардию.</li>
    <li>Одышка в покое, ортопноэ, отёки, быстрая прибавка массы тела.</li>
    <li>Синкопе, пресинкопе, устойчивая тахикардия или нарушения ритма.</li>
    <li>Неконтролируемое или впервые выявленное высокое АД, сочетание с острыми неврологическими симптомами.</li>
</ul>

